// 5.- Escribe un programa de tres líneas que pida un número, pida otro número y escriba el resultado de sumar estos dos números.
// Nota: Tener en cuenta la siguiente función: parseInt

let numberUno = parseInt(prompt('Ingrese el primer numero'));
let numberDos = parseInt(prompt('Ingrese el segundo numero'));

console.log(numberUno);
console.log(numberDos);

document.write('<h3>El resultado de su suma es: ' + (numberUno + numberDos) + '</h3>');


